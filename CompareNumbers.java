package com.stepwisedesign;

public class CompareNumbers {


    /* use this class to compare numbers and return a bool */

    private boolean didMatch;

    public boolean compareTwoInts(int number0, int number01){

        if (number0 == number01){

            didMatch = number0 == number01;
            return didMatch;

        } else {

            didMatch = number0 == number01;
            return didMatch;
        }
    }
}
